package com.product.Model;

public class Customer {

	
	int id;
	String name;
	int billEle;
	int billCl;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getBillEle() {
		return billEle;
	}
	public void setBillEle(int billEle) {
		this.billEle = billEle;
	}
	public int getBillCl() {
		return billCl;
	}
	public void setBillCl(int billCl) {
		this.billCl = billCl;
	}
	
	
	
}
