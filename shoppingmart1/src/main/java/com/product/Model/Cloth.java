package com.product.Model;

public class Cloth {

	int id;
	int pid;
	String name;
	String desc;
	float cost;
	int qunat;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public float getCost() {
		return cost;
	}
	public void setCost(float cost) {
		this.cost = cost;
	}
	public int getQunat() {
		return qunat;
	}
	public void setQunat(int qunat) {
		this.qunat = qunat;
	}
	
	
}
